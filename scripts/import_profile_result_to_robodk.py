"""
Import a ProfileEvaluationResult JSON into the live RoboDK station.

The result must include pose_rows. Results generated by
scripts/validate_yz_nullspace_demo.py already do.
"""
from __future__ import annotations

import argparse
import sys
from pathlib import Path

PROJECT_ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(PROJECT_ROOT))

from main import FRAME_NAME, ROBOT_NAME
from src.robodk_runtime.result_import import (
    import_profile_result_to_robodk,
    load_profile_result,
)


def _parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Import a solved profile result into the current RoboDK station."
    )
    parser.add_argument("--result", required=True, help="ProfileEvaluationResult JSON path.")
    parser.add_argument("--result-index", type=int, default=0)
    parser.add_argument("--robot", default=ROBOT_NAME)
    parser.add_argument("--frame", default=FRAME_NAME)
    parser.add_argument("--prefix", default="YZNS")
    parser.add_argument("--program-name", default=None)
    parser.add_argument("--focus-start-label", default=None)
    parser.add_argument("--focus-end-label", default=None)
    parser.add_argument("--move-type", choices=("MoveJ", "MoveL"), default=None)
    parser.add_argument("--no-program", action="store_true")
    parser.add_argument(
        "--cartesian-markers",
        action="store_true",
        help="Also create visible Cartesian marker targets for diagnostics.",
    )
    parser.add_argument("--no-cartesian-markers", action="store_true")
    parser.add_argument("--keep-existing", action="store_true")
    return parser.parse_args()


def main() -> int:
    args = _parse_args()
    result = load_profile_result(args.result, result_index=args.result_index)
    summary = import_profile_result_to_robodk(
        result,
        robot_name=args.robot,
        frame_name=args.frame,
        program_name=args.program_name,
        prefix=args.prefix,
        clear_prefix=not args.keep_existing,
        create_program=not args.no_program,
        create_cartesian_markers=bool(args.cartesian_markers and not args.no_cartesian_markers),
        focus_start_label=args.focus_start_label,
        focus_end_label=args.focus_end_label,
        move_type=args.move_type,
    )
    print(
        "RoboDK import complete: "
        f"program={summary.program_name}, "
        f"markers={summary.marker_count}, "
        f"program_targets={summary.program_target_count}, "
        f"robot={summary.robot_name}, frame={summary.frame_name}, prefix={summary.prefix}"
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
